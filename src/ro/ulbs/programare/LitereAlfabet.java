package ro.ulbs.programare;

public class LitereAlfabet {

    public void afisareLitere(){

        for (char c = 'a'; c <= 'z'; c++) {
            System.out.print(c);
        }

        System.out.println();

        for (char c = 'A'; c <= 'Z'; c++) {
            System.out.print(c);
        }

        System.out.println();

    }

}
